# Убираем дублирование "Ответ: " в начале ответа и другие ненужные фразы
def clean_answer(answer):
    # Убираем дублирование "Ответ: "
    if answer.lower().startswith("ответ: "):
        answer = answer[7:].strip()  # Убираем первые 7 символов ("Ответ: ")

    # Убираем ненужные фразы, такие как "*Примечание: это примерный ответ..." и "в зависимости от контекста диалога"
    unnecessary_phrases = [
        "*Примечание",
        "это примерный ответ на вопрос клиента",
        "в зависимости от контекста диалога, ответ может незначительно отличаться.",
    ]

    # Убираем любую ненужную фразу из ответа
    for phrase in unnecessary_phrases:
        if phrase.lower() in answer.lower():
            answer = answer.split(phrase)[0].strip()

    return answer
